package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon");  // executed 1 time
        }
        System.out.println("----------");

        while (true){  //repeated if
            System.out.println("Wooden Spoon"); //false olana kadar sonsuza kadar execute olur. false olacak biti≈ü eklemelisin
        }


    }
}
